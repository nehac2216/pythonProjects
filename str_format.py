# The Format Method Substitution
# Type1-
name= 'neha'
age= 23
print('{0} is {1} years old'.format(name,age))

# Type2-
name= 'titu'
age= 19
print('{name} is {age} years old'.format(name=name,age=age))

# Type3-
name= 'shinuu'
age= 22
print('{} is {} years old'.format(name,age))

# Type4-
name= 'asif'
age= 25
print(f'{name} is {age} years old')
